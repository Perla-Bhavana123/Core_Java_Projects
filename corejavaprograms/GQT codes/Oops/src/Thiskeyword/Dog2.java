package Thiskeyword;

public class Dog2 {
	private String name;
	private String color;
	private int age;
	private int cost;
	
	Dog2(String name,String color,int age,int cost){
		this.name=name;
		this.color=color;
		this.age=age;
		this.cost=cost;
	}
	String getName() {
		return name;
	}
	String getColor() {
		return color;
	}
	int getAge() {
		return age;
	}
	int getCost() {
		return cost;
	}
	

}
