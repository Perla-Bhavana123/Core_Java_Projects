package Thiskeyword;

public class Dog3 {

	private String name;
	private String color;
	private int age;
	private int cost;
	public Dog3(String name, String color, int age, int cost) {
		super();
		this.name = name;
		this.color = color;
		this.age = age;
		this.cost = cost;
	}
	public String getName() {
		return name;
	}
	public String getColor() {
		return color;
	}
	public int getAge() {
		return age;
	}
	public int getCost() {
		return cost;
	} 
	
	
}


