package Thiskeyword;

public class Employe1 {
	private String name;
	private String Em_id;
	private int age;
	private int salary;
	public Employe1(String name, String em_id, int age, int salary) {
		super();
		this.name = name;
		 this.Em_id = em_id;
		this.age = age;
		this.salary = salary;
	}
	public String getName() {
		return name;
	}
	public String getEm_id() {
		return Em_id;
	}
	public int getAge() {
		return age;
	}
	public int getSalary() {
		return salary;
	}
	
	

}


